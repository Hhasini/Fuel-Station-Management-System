    private void aAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aAddActionPerformed
        // add btn-assets

        String Description = aDes1.getText();
        Double Amount = Double.parseDouble(aAmount1.getText());
        String Type=aTcombo1.getSelectedItem().toString();
        String notes=aNotes1.getText();

        if(val.isPriceValue("Amount", Double.toString(Amount))&& val.isNotEmpty("Amount",Double.toString(Amount) ) && val.isNotEmpty(Description, Description) ){
            try{
                String x="INSERT INTO Assets_and_Liabilities (Description,Amount,Type,Notes) VALUES ('"+Description+"','"+Amount+"','"+Type+"','"+notes+"')";
                pst=conn.prepareStatement(x);
                pst.execute();

                tableload();

                JOptionPane.showMessageDialog(null, "Record successfuly Inserted");

            }

            catch(Exception e){
            }

        }
    }//GEN-LAST:event_aAddActionPerformed

